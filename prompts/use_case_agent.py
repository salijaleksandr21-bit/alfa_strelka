from core.llm_client import call_llm

def generate_use_cases(bt: str, bp: str) -> str:
    prompt = f"""
Ты — системный аналитик. На основе бизнес-требований (БТ) и бизнес-процесса (БП) создай Use Cases в формате Markdown.

Бизнес-требования:
{bt}

Бизнес-процесс:
{bp}

Каждый use case должен содержать:
- ID (UC-01, UC-02...)
- Название
- Актор
- Предусловие
- Основной поток (нумерованные шаги)
- Альтернативные потоки (если есть)
- Постусловие
- Ссылку на источник (ID бизнес-требования или номера шагов БП)

Формат — строго Markdown, с заголовками второго уровня (## UC-01: Название).
Не добавляй лишних комментариев, только сам документ.
"""
    return call_llm(prompt, system_prompt="Ты — эксперт по написанию Use Cases.")