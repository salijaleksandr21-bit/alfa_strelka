from core.llm_client import call_llm

def generate_nonfunctional(bt: str, bp: str, features: str = "") -> str:
    features_part = f"\nДополнительные характеристики (Features):\n{features}" if features else ""
    prompt = f"""
На основе бизнес-требований (БТ) и бизнес-процесса (БП) сформулируй минимум 3 нефункциональных требования (НФТ). {features_part}

Требования должны относиться к разным категориям, например:
- Производительность (время отклика)
- Удобство использования (интуитивность, адаптивность)
- Совместимость (браузеры, устройства)
- Надёжность (обработка ошибок)
- Безопасность (если уместно)

БТ:
{bt}

БП:
{bp}

Формат вывода — Markdown с заголовком "# Нефункциональные требования" и списком требований. Каждое требование должно начинаться с `- **НФТ-01: Категория**` и далее описание. Если уместно, добавь ссылку на БТ или Features.
"""
    return call_llm(prompt, system_prompt="Ты — эксперт по нефункциональным требованиям.")