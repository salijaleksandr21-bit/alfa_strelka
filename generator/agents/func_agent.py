from core.llm_client import call_llm

def generate_functional(bt: str, bp: str, features: str = "", use_cases: str = "", nft: str = "") -> str:
    context = f"""
Бизнес-требования (БТ):
{bt}

Бизнес-процесс (БП):
{bp}
"""
    if features:
        context += f"\nДополнительные характеристики (Features):\n{features}\n"
    if use_cases:
        context += f"\nUse Cases:\n{use_cases}\n"
    if nft:
        context += f"\nНефункциональные требования:\n{nft}\n"

    prompt = f"""
Ты — аналитик. На основе предоставленных артефактов создай детализированные функциональные требования (ФТ) в формате Markdown.

{context}

Каждое ФТ должно содержать:
- ID (ФТ-01, ФТ-02...)
- Описание (что система должна делать)
- Входные данные
- Ожидаемый результат
- Ссылка на источник (БТ-XX, UC-XX или Features)

Все ФТ должны быть прослеживаемыми: каждое ФТ ссылается на соответствующий пункт БТ или UC.
Формат — Markdown, таблица или списки. Пример:

### ФТ-01: Выполнение сложения
- **Описание:** При нажатии на кнопку "+" система складывает два введённых числа.
- **Входные данные:** Два числа, операция "+"
- **Ожидаемый результат:** Отображение суммы на экране
- **Источник:** БТ-01, UC-01

Создай минимум 5 ФТ, покрывающих все обязательные БТ и основные потоки БП.
"""
    return call_llm(prompt, system_prompt="Ты — эксперт по функциональным требованиям.")